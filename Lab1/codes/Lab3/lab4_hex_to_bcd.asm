	AREA RESET, DATA, READONLY
	EXPORT __Vectors
	
__Vectors
	DCD 0x10001000
	DCD Reset_Handler
	ALIGN
	AREA mycode, CODE, READONLY
	ENTRY
	EXPORT Reset_Handler
	
Reset_Handler
	LDR R0, =N
	LDR R0, [R0]
	LDR R1, =M
	LDR R2, =0xA
	LDR R3, =0;QUOTIENT
	MOV R4, R0; COPY OF NUMBER
	
LOOP 
	BL DIVIDE
	MUL R5, R3, R2
	SUBS R6, R4, R5
	STR R6, [R1], #4
	MOV R0, R3
	MOV R4, R0
	MOV R3, #0
	CMP R0, R2
	BHS LOOP
	
DIVIDE
	CMP R0, R2
	SUBHS R0, R2
	ADDHS R3, #1
	BHS DIVIDE
	BX LR
	
	STR R4, [R1]
	
STOP 
	B STOP
	
N DCD 0x64
	AREA mydata, DATA, READWRITE
M DCD 0
	END